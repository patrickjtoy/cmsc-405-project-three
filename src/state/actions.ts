// Cone
export const SET_CONE_COLOR = "SET_CONE_COLOR"
export const TOGGLE_CONE_VISIBILITY = "TOGGLE_CONE_VISIBILITY"

export const setConeColor = (color: number) => ({
  type: SET_CONE_COLOR,
  payload: color
})

export const toggleConeVisibility = () => ({
  type: TOGGLE_CONE_VISIBILITY
})

